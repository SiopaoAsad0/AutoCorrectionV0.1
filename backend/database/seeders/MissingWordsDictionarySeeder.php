        $words = $this->missingWords();

        $batch = [];
        foreach ($words as $entry) {
            $batch[] = array_merge($entry, [
                'created_at' => $now,
                'updated_at' => $now,
            ]);
            if (count($batch) >= self::INSERT_CHUNK) {
                DB::table('dictionaries')->insertOrIgnore($batch);
                $batch = [];
            }
        }
        if (!empty($batch)) {
            DB::table('dictionaries')->insertOrIgnore($batch);
        }
    }

